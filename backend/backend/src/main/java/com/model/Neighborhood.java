package com.model;

public class Neighborhood {
    private String name;
    private String city;
    private double crime;
    private int schools;
    private int nightlife;
    private int greenery;
    private int affordability;
    private int transport;

    // Getters and Setters
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public double getCrime() {
        return crime;
    }

    public void setCrime(double crime) {
        this.crime = crime;
    }

    public int getSchools() {
        return schools;
    }

    public void setSchools(int schools) {
        this.schools = schools;
    }

    public int getNightlife() {
        return nightlife;
    }

    public void setNightlife(int nightlife) {
        this.nightlife = nightlife;
    }

    public int getGreenery() {
        return greenery;
    }

    public void setGreenery(int greenery) {
        this.greenery = greenery;
    }

    public int getAffordability() {
        return affordability;
    }

    public void setAffordability(int affordability) {
        this.affordability = affordability;
    }

    public int getTransport() {
        return transport;
    }

    public void setTransport(int transport) {
        this.transport = transport;
    }
}




